--
-- Name: get_community_competitions(uuid, uuid); Type: FUNCTION; Schema: public; Owner: postgres
--

CREATE FUNCTION public.get_community_competitions(p_community_id uuid, p_user_id uuid) RETURNS SETOF public.competitions
    LANGUAGE plpgsql SECURITY DEFINER
    AS $$
BEGIN
    RETURN QUERY
    SELECT DISTINCT c.*
    FROM competitions c
    WHERE c.community_id = p_community_id
    AND (
        -- User is the creator of the competition
        c.created_by = p_user_id
        OR
        -- User is the creator of the community
        EXISTS (
            SELECT 1
            FROM communities comm
            WHERE comm.id = c.community_id
            AND comm.created_by = p_user_id
        )
        OR
